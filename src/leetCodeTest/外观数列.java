package leetCodeTest;

public class 外观数列 {
    public static void main(String[] args) {
        int n = 4;
        String s = countAndSay(n);
        System.out.println("s = " + s);
    }
    public static String countAndSay(int n) {
        if (n == 1)
            return "1";
        return "1";
    }
}
